%DFS
dfs(Source,Destination,Distance,[Source|Path]):-
	dfs(Source, Destination, [Source], Distance, Path).

dfs(Destination, Destination,_, 0, []) :- !.

dfs(Source, Destination, Visited, Distance, [Next|Path]):-
	connectedBothWays(Source, Next, D0),
	\+memberchk(Next, Visited),
	dfs(Next, Destination, [Next|Visited], D1, Path),
	Distance is D0 + D1.

connectedBothWays(City1,City2,Dist) :- route(City1,City2,Dist).
connectedBothWays(City1,City2,Dist) :- route(City2,City1,Dist).

%BestFirstSearch

next_node(Present, Next, End, Path, Cost) :- heuristic(Present, Next, End, Cost), 
    connectedBothWays(Present, Next, C0), 
    \+memberchk(Next, Path).

plist([]) :- write(' Destination Reached.!'),nl. 
plist([H|T]):- write(H), write(' -> '),plist(T).

best_f(End, End, C, [End], Cost) :-
    write('Cost: '),
    write(Cost), nl, nl,
    reverse(C, C1), 
    plist(C1),nl.
best_f(Begin, End, Visited, [Begin|Path], Cst) :- 
    next_node(Begin, Next_node, End, Visited, Cost), 
    MinCost is Cst+Cost,
    best_f(Next_node, End, [Next_node|Visited], Path, MinCost).

bestFS(Begin,End,Path) :- best_f( Begin, End, [Begin], Path, 0).

